export class Card {
  public title: string = "";
  public subTitle: string = "";
  public text: string ="";
  public imagePath: string ="";


  constructor(title: string, subTitle: string, text: string, imagePath: string) {
    this.title = title;
    this.subTitle = subTitle;
    this.text = this.text;
    this.imagePath = this.imagePath;
  }
}
